package az.edu.turing.module0.lessonForLoops;

public class ForLoopsApp {
    public static void main(String[] args) {
        int x = 1;
        do {
            x++;
            System.out.println(x);
        } while(x < 2);

        int y = 1;

        while (y<2){

            y++;
            System.out.println(y);




        }

        for (int i = 2;i<5;i = i*i){
            System.out.println(i);
        }
    }
}
